package tank;

public class Zhuankuai {
	private int x,y;
	boolean sm=true;
	
	public boolean getSm() {
		return sm;
	}


	public void setSm(boolean sm) {
		this.sm = sm;
	}


	public int getX() {
		return x;
	}


	public void setX(int x) {
		this.x = x;
	}


	public int getY() {
		return y;
	}


	public void setY(int y) {
		this.y = y;
	}


	Zhuankuai(int x,int y){
		this.x=x;
		this.y=y;
	}
}
